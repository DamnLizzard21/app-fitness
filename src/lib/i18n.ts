export function useTranslation(locale: 'pt-BR' | 'en' | 'es' = 'pt-BR') {
  const t = (key: string): string => {
    const translation = translations[key]
    if (!translation) {
      console.warn(`Translation missing for key: ${key}`)
      return key
    }
    return translation[locale] || translation['pt-BR'] || key
  }

  return { t }
}